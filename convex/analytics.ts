// ─── HELPERS ────────────────────────────────────────────────────────────────

function getDateStr(offsetDays = 0): string {
    const d = new Date();
    d.setUTCDate(d.getUTCDate() + offsetDays);
    return d.toISOString().slice(0, 10); // "YYYY-MM-DD"
}

function getSessionId(): string {
    // Client-side session ID — generated by AnalyticsProvider and stored in localStorage.
    // Stays stable for the browser tab session.
    return Math.random().toString(36).slice(2);
}

// ─── VISITOR ID ────────────────────────────────────────────────────────────

/** Stable anonymous visitor ID stored in localStorage under `sg_visitor_id`. */
function getVisitorId(): string {
    // On the client, this is read from localStorage by the AnalyticsProvider.
    // Server-side, we accept a visitorId arg — no access to localStorage.
    return "";
}

