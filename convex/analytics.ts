/** Record first-touch UTM data when a visitor lands on the site. */
export const recordUtmEvent = mutation({
    args: {
        visitorId: v.string(),
        utmSource: v.optional(v.string()),
        utmMedium: v.optional(v.string()),
        utmCampaign: v.optional(v.string()),
        utmTerm: v.optional(v.string()),
        utmContent: v.optional(v.string()),
        referringDomain: v.optional(v.string()),
        landingPage: v.string(),
    },
    handler: async (ctx, args) => {
        // Only record if at least one UTM param is present
        if (!args.utmSource && !args.utmMedium && !args.utmCampaign) {
            return null;
        }

        const id = await ctx.db.insert("utm_events", {
            visitorId: args.visitorId,
            utmSource: args.utmSource ?? undefined,
            utmMedium: args.utmMedium ?? undefined,
            utmCampaign: args.utmCampaign ?? undefined,
            utmTerm: args.utmTerm ?? undefined,
            utmContent: args.utmContent ?? undefined,
            referringDomain: args.referringDomain ?? undefined,
            landingPage: args.landingPage,
            userId: undefined,
            createdAt: Date.now(),
        });

        return id;
    },
});

/** Backfill userId on an existing UTM event after signup.
 * If userId is not provided, resolves it from the current auth context.
 */
export const linkUtmToUser = mutation({
    args: {
        visitorId: v.string(),
        utmEventId: v.optional(v.id("utm_events")),
    },
    handler: async (ctx, args) => {
        // Resolve userId from auth context if not provided
        let userId = await getAuthUserId(ctx);
        if (!userId) return;

        if (args.utmEventId) {
            await ctx.db.patch(args.utmEventId, { userId });
            return;
        }
        // Fallback: find the most recent UTM event for this visitor and link it
        const event = await ctx.db
            .query("utm_events")
            .withIndex("by_visitor_id", (q) => q.eq("visitorId", args.visitorId))
            .first();
        if (event) {
            await ctx.db.patch(event._id, { userId });
        }
    },
});

