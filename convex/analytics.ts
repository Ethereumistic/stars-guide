/** Daily signups grouped by channel (UTM source). */
export const dailySignupsByChannel = query({
    args: { startDate: v.string(), endDate: v.string() },
    handler: async (ctx, args) => {
        const events = await ctx.db
            .query("feature_events")
            .withIndex("by_date", (q) =>
                q.gte("eventDate", args.startDate).lte("eventDate", args.endDate)
            )
            .collect();

        // Filter to signup events client-side (compound index not available for date-only)
        const signupEvents = events.filter((e) => e.eventName === "signup_completed");

        // Group signups by UTM source
        const byChannel: Record<string, number> = {};
        for (const evt of signupEvents) {
            if (evt.utmEventId) {
                const utm = await ctx.db.get(evt.utmEventId);
                const src = utm?.utmSource ?? "direct";
                byChannel[src] = (byChannel[src] ?? 0) + 1;
            } else {
                byChannel["direct"] = (byChannel["direct"] ?? 0) + 1;
            }
        }

        // Group signups by date
        const byDate: Record<string, number> = {};
        for (const evt of signupEvents) {
            byDate[evt.eventDate] = (byDate[evt.eventDate] ?? 0) + 1;
        }

        return {
            total: signupEvents.length,
            byChannel,
            byDate,
        };
    },
});

/** Weekly active users count (distinct users with any activity in a date range). */
export const weeklyActiveUsers = query({
    args: { startDate: v.string(), endDate: v.string() },
    handler: async (ctx, args) => {
        const rows = await ctx.db
            .query("user_activity")
            .withIndex("by_date", (q) =>
                q.gte("date", args.startDate).lte("date", args.endDate)
            )
            .collect();

        const uniqueUsers = new Set(rows.map((r) => r.userId.toString()));
        return {
            wau: uniqueUsers.size,
            totalSessions: rows.reduce((s, r) => s + r.sessionsCount, 0),
            totalDurationMs: rows.reduce((s, r) => s + r.totalSessionDurationMs, 0),
        };
    },
});

/** Top traffic sources by UTM campaign (signups attributed). */
export const topTrafficSources = query({
    args: { limit: v.optional(v.number()) },
    handler: async (ctx, args) => {
        const limit = args.limit ?? 10;
        const events = await ctx.db
            .query("feature_events")
            .withIndex("by_event_date", (q) =>
                q.eq("eventName", "signup_completed")
            )
            .collect();

        const campaignCounts: Record<string, number> = {};
        for (const evt of events) {
            if (evt.utmEventId) {
                const utm = await ctx.db.get(evt.utmEventId);
                if (utm?.utmCampaign) {
                    campaignCounts[utm.utmCampaign] =
                        (campaignCounts[utm.utmCampaign] ?? 0) + 1;
                }
            }
        }

        return Object.entries(campaignCounts)
            .sort(([, a], [, b]) => b - a)
            .slice(0, limit)
            .map(([campaign, count]) => ({ campaign, count }));
    },
});

/** Referral program summary: pending, completed, total stardust. */
export const referralProgramSummary = query({
    args: {},
    handler: async (ctx) => {
        const allReferrals = await ctx.db.query("referrals").collect();
        const completed = allReferrals.filter((r) => r.status === "completed");
        const pending = allReferrals.filter((r) => r.status === "pending");
        return {
            total: allReferrals.length,
            completed: completed.length,
            pending: pending.length,
            totalStardustAwarded: completed.reduce((s, r) => s + r.rewardAmount, 0),
        };
    },
});

/** Full dashboard snapshot for a date range — used by weekly report. */
export const dashboardSnapshot = query({
    args: {
        startDate: v.string(),
        endDate: v.string(),
    },
    handler: async (ctx, args) => {
        // Signups by channel
        const signupEvents = await ctx.db
            .query("feature_events")
            .withIndex("by_event_date", (q) =>
                q
                    .eq("eventName", "signup_completed")
                    .gte("eventDate", args.startDate)
                    .lte("eventDate", args.endDate)
            )
            .collect();

        const byChannel: Record<string, number> = { direct: 0 };
        const byDate: Record<string, number> = {};
        for (const evt of signupEvents) {
            const date = evt.eventDate;
            byDate[date] = (byDate[date] ?? 0) + 1;
            if (evt.utmEventId) {
                const utm = await ctx.db.get(evt.utmEventId);
                const src = utm?.utmSource ?? "direct";
                byChannel[src] = (byChannel[src] ?? 0) + 1;
            } else {
                byChannel["direct"]++;
            }
        }

        // WAU
        const activityRows = await ctx.db
            .query("user_activity")
            .withIndex("by_date", (q) =>
                q.gte("date", args.startDate).lte("date", args.endDate)
            )
            .collect();
        const uniqueUsers = new Set(activityRows.map((r) => r.userId.toString()));

        // Top features used — use by_date index (date-only, no eventName filter)
        const featureEvents = await ctx.db
            .query("feature_events")
            .withIndex("by_date", (q) =>
                q.gte("eventDate", args.startDate).lte("eventDate", args.endDate)
            )
            .collect();
        const featureCounts: Record<string, number> = {};
        for (const evt of featureEvents) {
            featureCounts[evt.eventName] = (featureCounts[evt.eventName] ?? 0) + 1;
        }
        const topFeatures = Object.entries(featureCounts)
            .sort(([, a], [, b]) => b - a)
            .slice(0, 10)
            .map(([name, count]) => ({ name, count }));

        // Referrals
        const allReferrals = await ctx.db.query("referrals").collect();
        const completed = allReferrals.filter((r) => r.status === "completed").length;
        const pending = allReferrals.filter((r) => r.status === "pending").length;

        // Subscription tiers
        const allUsers = await ctx.db.query("users").collect();
        const byTier = { free: 0, popular: 0, premium: 0 };
        for (const u of allUsers) {
            byTier[u.tier] = (byTier[u.tier] ?? 0) + 1;
        }

        return {
            signups: { total: signupEvents.length, byChannel, byDate },
            wau: uniqueUsers.size,
            topFeatures,
            referrals: { total: allReferrals.length, completed, pending },
            byTier,
        };
    },
});

