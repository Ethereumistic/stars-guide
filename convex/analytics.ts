/**
 * Record a named feature event.
 * Call this from any mutation or client-side hook for key conversion actions.
 *
 * eventName examples:
 *   signup_completed, onboarding_finished, oracle_first, oracle_message,
 *   journal_entry, upgrade_to_popular, upgrade_to_premium, referral_completed,
 *   dashboard_viewed, daily_horoscope_viewed, cosmic_weather_viewed
 */
export const recordFeatureEvent = mutation({
    args: {
        eventName: v.string(),
        userId: v.optional(v.id("users")),
        sessionId: v.string(),
        metadata: v.optional(v.record(v.string(), v.string())),
        utmEventId: v.optional(v.id("utm_events")),
        funnelStage: v.optional(v.union(
            v.literal("signup"),
            v.literal("activation"),
            v.literal("engagement"),
            v.literal("revenue"),
        )),
    },
    handler: async (ctx, args) => {
        const now = Date.now();
        const today = new Date(now).toISOString().slice(0, 10);

        const id = await ctx.db.insert("feature_events", {
            eventName: args.eventName,
            userId: args.userId ?? undefined,
            eventDate: today,
            timestamp: now,
            sessionId: args.sessionId,
            metadata: args.metadata ?? undefined,
            utmEventId: args.utmEventId ?? undefined,
            funnelStage: args.funnelStage ?? undefined,
        });

        return id;
    },
});

/**
 * Backfill userId onto feature events that were recorded anonymously.
 * Called after signup to attribute pre-auth events.
 */
export const backfillUserIdOnEvents = mutation({
    args: { userId: v.id("users"), sessionId: v.string() },
    handler: async (ctx, args) => {
        const anonymousEvents = await ctx.db
            .query("feature_events")
            .withIndex("by_user_event", (q) =>
                q.eq("userId", undefined).eq("eventName", "session_start")
            )
            .filter((q) => q.eq(q.field("sessionId"), args.sessionId))
            .collect();

        for (const evt of anonymousEvents) {
            await ctx.db.patch(evt._id, { userId: args.userId });
        }
    },
});

